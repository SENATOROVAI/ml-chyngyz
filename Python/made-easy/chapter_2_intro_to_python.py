# #### Interpreter
#
# By definition, an interpreter is a type of computer program, which directly executes instructions written in a programming language.
#
# There are 7 popular Interpreters:
# * IPython - default Interpreter, that comes with Python.
# * CPython - one of the most popular, written on C and Python. CPython can be considered a compiler, because it converts Python code into bytecode before interpretation.
# * IronPython - this is an implementation of a programming language Python for the platform .NET, which uses Python and .NET libraries.
# * Jython - works on Java platform, combination Java and Python, converts Python code into Java byte-code. allows you to run Python code on any machine on which you have installed JVM.
# * PyPy - this Python interpreter is about 4.4 times
# faster than CPython
# * PythonNet
