# Python 3 is regarded as the future of Python and is the version of the language that is currently in development.
# The focus of Python 3 development was to clean up the codebase and remove redundancy, making it clear that there was only one way to perform a given task. Major modifications to Python 3.0 included changing the print statement into a built-in function, improve the way integers are divided, and providing more Unicode support.
