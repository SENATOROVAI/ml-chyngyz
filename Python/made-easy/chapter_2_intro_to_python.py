# #### Python Origin
#
# Python was conceived in the late 1980s by Guido van Rossum at Centrum Wiskunde & Informatics (CWI) in the Netherlands as a successor to the ABC language. Its implementation began in December 1989.
#
# Python 2.0 was released on 16 October 2000 with many major new features, including a cycle-detecting garbage collector and support for Unicode.
#
# Python 3.0 was released on 3 December 2008.
#
# Python 2.7's end-of-life date was initially set at 2015 then postponed to 2020 out of concern that a large body of existing code could not easily be forward-ported to Python 3
#
# ##### Python as compared to other Programming Languages
#
# Python is simple to use, offering much more structure and support for large programs than shell scripts or batch files can offer.
#
# Python allows you to split your program into modules that can be reused in other Python programs. It comes with a large collection of standard modules that you can use as the basis of your programs
#
# Python is an interpreted language, it can be used interactively, which makes it easy to experiment with features of the language.
#
# Programs written in Python are typically much shorter than equivalent C, C++, or Java programs.
#
#
# ##### Philosophy of Python
#
# The language's core philosophy is:
#
# * Beautiful is better than ugly
# * Explicit is better than implicit
# * Simple is better than complex
# * Complex is better than complicated
# * Readability counts

# #### Advantage of Python over other languages
#
# * Simple - reading a python code feels like reading english. Simplicity is one of the core strengths of Python.
#
# * Easy to learn - syntax is extremely simple.
#
# * Free and Open Source - Python is FREE, Python is an example of a FLOSS (Free/Libr√© and Open Source Software).
#
# * High Level Language -  not worry about low-level details such as managing the memory used by your program, high-level programming languages closer to human languages.
#
# * Portable - Python can be used on Windows, GNU/Linux, Macintosh, Solaris, Palm OS, PlayStation, Sharp Zaurus, Windows CE and PocketPC!
#
# * Interpreted - the program is run directly from the source code. Internally, Python converts the source code into an intermediate form called bytecode and then translates this into the native language of the computer and then runs it.
#
# * What is an Interpreter?
#
# * By definition, an interpreter is a type of computer program that directly executes the instructions written in some programming or scripting language. By direct execution, we mean that it doesn't require the instructions to be compiled into a machine language program first.
