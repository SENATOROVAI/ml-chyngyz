# #### Настоящее и будущее Data Science
#
# Сегодня развитие Data Science идет по нескольким веткам.
#
# * **`Искуственный интелект`** - сфера создания интеллектуальных машин, способных работать и принимать решения как человек. Исследования в этой области начались в 1936 г., Алан Тьюринг создал первые машины на базе ИИ.
#
# * **`Машинное обучение`** - создание различных моделей, об**учение с учителем и** обучение без учителя.
#
# * **`Глубокое обучение`** - создание многослойной нейросети, наличие более одного скрытого слоя нейронов в сети, которые проводят математические вычесления.

