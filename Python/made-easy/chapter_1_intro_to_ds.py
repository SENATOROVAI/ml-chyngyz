# #### Основы компьютерного программирования
#
# Программирование это общение с компьюетером, пошаговые инструкции, понятные компьютеру.
#
# Пошаговые инструкции называются **алгортимом**.
#
# **Алгоритм** - последовательность четко определенных действий.
#
# Чем проще и оптимальнее алгоритм тем лучше и быстрее результат.
#
# Алгоритмы состоят из 3 видов операторов.
#
# 1. Последовательные операторы.
# 2. Условные операторы.
# 3. Циклы или итерации.
#
# * **Последовательные операторы** - выполняются один за другим, последовательно.
# * **Условные операторы** - выполняются в зависимости от определеннго условия.
# * **Циклы или итерации** - определенный набор действий выполняется снова и снова в зависимости от некоторого условия.
#
#
# ##### Языки программирования
#
# Существует огромное количество языков программирования, по аналогии с разговорными языками, у всех языков программирования есть свои сильные и слабые стороны в зависимости от задач.
#
# Компьютер понимает только **машинный язык**, языки программирования созданы что бы человек мог взаимодействовать с машиной.
#
# Все языки программирования являются **языками выского уровня**
#
#
# ##### Исходный код
#
# Исходный код - то что пишут программисты на всех языках программирования. Исходный код не может содержать разметки и форматирования.
#
# Что бы машина понимала исходный код необходимо перевести код на понятный для машины язык. Для этого в помощь нам приходят такие понятия как ***компиляция*** и ***интерпретация***
#
# **Компилятор** - это прогармма, преобразующая исходный код в машинный. Комплияция языка программирования высокго уровня на машинный язык.
#
# **Интерпретатор** - это программа которая преобразует исходный код в машинный при помощи построчного чтения кода, при каждом запуске исходного кода.
#
# ***Наш любимый Python использует комбинацию обоих методов.***
#
